from pathlib import Path
from typing import Dict, Optional
from utils.logger import get_logger
from utils.helpers import generate_project_id, ensure_directory, safe_write_file
from utils.errors import FileOperationException
from config.settings import settings

logger = get_logger(__name__)


class FileWriter:
    """Handle file generation and project structure creation."""

    # Default requirements for generated FastAPI projects
